    public void updateTable() {
        total = 0f;
        Cesta cesta = null;
        cesta = ss.getCesta();
        if (cesta != null) {
            List<Orden> ordenes = null;
            ordenes = cesta.getOrdenes();
            if (ordenes.size() == 0) {
                this.dispose();
                btn_finalizar_compra.setVisible(false);
                this.label_total.setText("$ 00.00");
                JOptionPane.showMessageDialog(null, "La cesta está vacía");
            }

            DefaultTableModel model = (DefaultTableModel) table_productos_cesta.getModel();
            int rowCount = model.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
                model.removeRow(i);
            }

            for (Orden orden : ordenes) {
                total += orden.getProducto().getCosto() * orden.getCantidad();
                Producto producto = orden.getProducto();
                Object[] row = {producto.getSku(), producto.getNombre(), producto.getDescripcion(), producto.getUrlImagen(), producto.getCosto(), orden.getCantidad()};
                model.addRow(row);
            }
            this.label_total.setText("$ " + total);
            cesta.setTotal(total);
            ss.saveCesta(cesta);
        }

    }


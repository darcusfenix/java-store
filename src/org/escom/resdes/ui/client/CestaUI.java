    private void btn_delete_producto_cestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delete_producto_cestaActionPerformed
        Integer index = table_productos_cesta.getSelectedRow();
        if (index >= 0) {
            DefaultTableModel model = (DefaultTableModel) table_productos_cesta.getModel();
            Producto producto = null;
            Integer skuIndex = (Integer) model.getValueAt(index, 0);

            Catalogo c = new Catalogo();
            c = ss.getCatalogo();
            List<Producto> productos = null;
            productos = c.getProductos();

            for (Producto item : productos) {
                if (item.getSku().equals(skuIndex)) {
                    producto = item;
                    break;
                }
            }
            cs.removeProductoOfCesta(producto);
            JOptionPane.showMessageDialog(null, "Se quitó el producto de la cesta");
        }
    }//GEN-LAST:event_btn_delete_producto_cestaActionPerformed

